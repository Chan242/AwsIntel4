
// 논리 연산자
public class OperatorBasic114 {

	public static void main(String[] args) {
//		and, 그리고
		System.out.println(true && true);
		System.out.println(true && false);
//		or, 또는
		System.out.println(true || true);
		System.out.println(false ||true);
		
		System.out.println("=====================");
		int x = 10;
		int y = 8;
		System.out.println(22 < 22 && x > y);
		System.out.println(22 >= y || x <= y);

	}
	
}

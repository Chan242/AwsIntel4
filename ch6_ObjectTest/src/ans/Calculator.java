package ans;


public class Calculator {

	int add(int a, int b) {
		int result = a + b;
		
		return result;
	}
	
	int subtract(int a, int b) {
		int result = a - b;
		
		return result;
	}
	
	int multiplication(int a, int b) {
		int result = a * b;
		
		return result;
	}
	
	double division(int a, int b) {
		double result = a / (double)b;
		
		return result;
	}
}
